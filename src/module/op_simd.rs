//! The fixed-width SIMD (`v128`) instruction set (#37), split from [`Op`](super::op::Op) so the
//! ~236 vector opcodes don't bloat the core enum. Reached via `Op::Simd(SimdOp)`; executed by the
//! `exec::simd` handler chain over portable scalar lanes (autovectorized by LLVM, zero `unsafe`).
//!
//! Variant names mirror `wasmparser::Operator` 1:1 so translation is mechanical. Immediates: a
//! memory arg, a lane index `u8`, the shuffle's 16 byte indices, or the raw `v128.const` bits.

